public class PileScore {
	private double pileScore;
	private double envirScore;
	private double facedScore;
	private double funcScore;
	private double servScore;

	public double getPileScore() {
		return pileScore;
	}

	public void setPileScore(double pileScore) {
		this.pileScore = pileScore;
	}

	public double getEnvirScore() {
		return envirScore;
	}

	public void setEnvirScore(double envirScore) {
		this.envirScore = envirScore;
	}

	public double getFacedScore() {
		return facedScore;
	}

	public void setFacedScore(double facedScore) {
		this.facedScore = facedScore;
	}

	public double getFuncScore() {
		return funcScore;
	}

	public void setFuncScore(double funcScore) {
		this.funcScore = funcScore;
	}

	public double getServScore() {
		return servScore;
	}

	public void setServScore(double servScore) {
		this.servScore = servScore;
	}
}
